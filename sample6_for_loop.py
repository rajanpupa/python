# range is from 0 to n-1
for i in range(5):
    print(i)

# another range example (11,12,13,14)
for j in range(11, 15):
    print(j)

# auto iterator for loop example
fruits = ["apple", "orange", "banana"]
for x in fruits:
    print(x)

# increment the range by 3, and else, example
for x in range(2, 30, 3):
  print(x)
else:
  print("Finally finished!")